/* ─── Hook ────────────────────────────────────────────── */

export function useTranslation() {
  const { state } = useApp();
  const lang = state.settings.language;

  const t = useCallback(
    (key: string, fallback?: string): string => {
      return translations[lang]?.[key] ?? translations['en']?.[key] ?? fallback ?? key;
    },
    [lang]
  );

  return { t, language: lang };
}
